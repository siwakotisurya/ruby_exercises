class Encryptor
	
	attr_writer :key

	def initialize
		@alphabets =  {'a' => 'n', 'b' => 'o', 'c' => 'p', 'd' => 'q',
     'e' => 'r', 'f' => 's', 'g' => 't', 'h' => 'u',
     'i' => 'v', 'j' => 'w', 'k' => 'x', 'l' => 'y',
     'm' => 'z', 'n' => 'a', 'o' => 'b', 'p' => 'c',
     'q' => 'd', 'r' => 'e', 's' => 'f', 't' => 'g',
     'u' => 'h', 'v' => 'i', 'w' => 'j', 'x' => 'k',
     'y' => 'l', 'z' => 'm'}
	end

	def word_breaker
		letter = @key.split("")
		letter.select do |n|
			puts @alphabets[n]
		end	
	end
end

e = Encryptor.new

e.key="num"

e.word_breaker